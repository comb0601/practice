/*
 * Lesson 01 - Example 08: Story Teller
 *
 * A creative example that tells a short story.
 * Demonstrates how to format narrative text with C++.
 *
 * Compile: g++ 08_StoryTeller.cpp -o StoryTeller
 */

#include <iostream>

int main()
{
    std::cout << "========================================\n";
    std::cout << "     THE JOURNEY OF A C++ STUDENT      \n";
    std::cout << "========================================\n";
    std::cout << "\n";

    std::cout << "Chapter 1: The Beginning\n";
    std::cout << "------------------------\n";
    std::cout << "Once upon a time, a curious student\n";
    std::cout << "decided to learn C++ programming.\n";
    std::cout << "They opened their first code editor\n";
    std::cout << "and typed 'Hello World!'...\n";
    std::cout << "\n";

    std::cout << "Chapter 2: The Challenge\n";
    std::cout << "------------------------\n";
    std::cout << "At first, the syntax seemed strange.\n";
    std::cout << "Semicolons, brackets, and namespaces...\n";
    std::cout << "But with each lesson, understanding grew.\n";
    std::cout << "The pieces started falling into place.\n";
    std::cout << "\n";

    std::cout << "Chapter 3: The Discovery\n";
    std::cout << "------------------------\n";
    std::cout << "Variables, functions, and classes...\n";
    std::cout << "Windows API, graphics, and more!\n";
    std::cout << "The student realized the true power\n";
    std::cout << "of C++ programming.\n";
    std::cout << "\n";

    std::cout << "Chapter 4: The Future\n";
    std::cout << "---------------------\n";
    std::cout << "With dedication and practice,\n";
    std::cout << "the student became a master.\n";
    std::cout << "Creating applications, games, and tools.\n";
    std::cout << "The journey continues...\n";
    std::cout << "\n";

    std::cout << "========================================\n";
    std::cout << "             THE END...                 \n";
    std::cout << "         ...OR JUST THE BEGINNING?      \n";
    std::cout << "========================================\n";

    return 0;
}
