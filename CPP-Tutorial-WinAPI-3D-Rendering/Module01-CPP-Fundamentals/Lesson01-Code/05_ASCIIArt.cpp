/*
 * Lesson 01 - Example 05: ASCII Art
 *
 * Demonstrates creating ASCII art using cout.
 * Shows how to use special characters and formatting.
 *
 * Compile: g++ 05_ASCIIArt.cpp -o ASCIIArt
 */

#include <iostream>

int main()
{
    std::cout << "=== ASCII Art Gallery ===\n\n";

    // Cat
    std::cout << "1. Cat:\n";
    std::cout << "    /\\_/\\  \n";
    std::cout << "   ( o.o ) \n";
    std::cout << "    > ^ <  \n";
    std::cout << "   /|   |\\ \n";
    std::cout << "  (_|   |_)\n";
    std::cout << "\n";

    // Smiley face
    std::cout << "2. Smiley Face:\n";
    std::cout << "   *****\n";
    std::cout << "  *     *\n";
    std::cout << " * O   O *\n";
    std::cout << " *   ^   *\n";
    std::cout << " *  \\_/  *\n";
    std::cout << "  *     *\n";
    std::cout << "   *****\n";
    std::cout << "\n";

    // House
    std::cout << "3. House:\n";
    std::cout << "      /\\\n";
    std::cout << "     /  \\\n";
    std::cout << "    /    \\\n";
    std::cout << "   /______\\\n";
    std::cout << "   |  __  |\n";
    std::cout << "   | |  | |\n";
    std::cout << "   | |  | |\n";
    std::cout << "   |_|__|_|\n";
    std::cout << "\n";

    // Arrow
    std::cout << "4. Arrow:\n";
    std::cout << "        *\n";
    std::cout << "       ***\n";
    std::cout << "      *****\n";
    std::cout << "     *******\n";
    std::cout << "        *\n";
    std::cout << "        *\n";
    std::cout << "        *\n";
    std::cout << "\n";

    // Banner
    std::cout << "5. Welcome Banner:\n";
    std::cout << " _    _      _                          \n";
    std::cout << "| |  | |    | |                         \n";
    std::cout << "| |  | | ___| | ___ ___  _ __ ___   ___ \n";
    std::cout << "| |/\\| |/ _ \\ |/ __/ _ \\| '_ ` _ \\ / _ \\\n";
    std::cout << "\\  /\\  /  __/ | (_| (_) | | | | | |  __/\n";
    std::cout << " \\/  \\/ \\___|_|\\___\\___/|_| |_| |_|\\___|\n";
    std::cout << "\n";

    // Diamond
    std::cout << "6. Diamond:\n";
    std::cout << "    *\n";
    std::cout << "   ***\n";
    std::cout << "  *****\n";
    std::cout << " *******\n";
    std::cout << "*********\n";
    std::cout << " *******\n";
    std::cout << "  *****\n";
    std::cout << "   ***\n";
    std::cout << "    *\n";

    return 0;
}
