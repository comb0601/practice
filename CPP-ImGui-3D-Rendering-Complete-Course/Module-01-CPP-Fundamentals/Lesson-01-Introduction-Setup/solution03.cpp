    std::cout << "        |      |" << std::endl;
    std::cout << "        | C++  |" << std::endl;
    std::cout << "        |      |" << std::endl;
    std::cout << "        |  17  |" << std::endl;
    std::cout << "        |______|" << std::endl;
    std::cout << "        |      |" << std::endl;
    std::cout << "       /|      |\\" << std::endl;
    std::cout << "      / |      | \\" << std::endl;
    std::cout << "     /  |      |  \\" << std::endl;
    std::cout << "    /___|______|___\\" << std::endl;
    std::cout << "        |  ||  |" << std::endl;
    std::cout << "       /|  ||  |\\" << std::endl;
    std::cout << "      / |  ||  | \\" << std::endl;
    std::cout << "        |  ||  |" << std::endl;
    std::cout << "       /|  ||  |\\" << std::endl;
    std::cout << "      / |__||__| \\" << std::endl;
    std::cout << "     /  |  ||  |  \\" << std::endl;
    std::cout << "    /___|__||__|___\\" << std::endl;
    std::cout << "       \\__/  \\__/" << std::endl;
